def get_ray_directions(H, W, K):
    uv = create_meshgrid(H, W, normalized_coordinates=False)[0]
    uv1 = torch.cat((uv, torch.ones_like(uv[:,:,:1])), 2) # (hw3)
    directions = uv1 @ torch.linalg.inv(K).T
    return directions

def get_rays(directions, c2w):
    rays_d = directions @ c2w[:, :3].T # (H, W, 3)
    rays_d = rays_d / torch.norm(rays_d, dim=-1, keepdim=True)
    rays_o = c2w[:, 3].expand(rays_d.shape) # (H, W, 3)
    rays_d = rays_d.view(-1, 3)
    rays_o = rays_o.view(-1, 3)

    return rays_o, rays_d

def get_camera_rays(directions):
    rays_d = directions
    rays_d = rays_d / torch.norm(rays_d, dim=-1, keepdim=True)
    rays_o = torch.Tensor([0.,0.,0.]).expand(rays_d.shape) # (H, W, 3)
    rays_d = rays_d.view(-1, 3)
    rays_o = rays_o.view(-1, 3)

    return rays_o, rays_d


